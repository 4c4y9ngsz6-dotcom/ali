let currentLang = localStorage.getItem('lang') || 'ar';
let originalTexts = new Map();
let originalAttrs = new Map();

function translateTextNodes(node) {
    if (node.nodeType === Node.TEXT_NODE) {
        let text = node.nodeValue.trim();
        if (text) {
            if (!originalTexts.has(node)) {
                originalTexts.set(node, text);
            }
            
            let sourceText = originalTexts.get(node);
            if (currentLang === 'en' && dictionary[sourceText]) {
                node.nodeValue = node.nodeValue.replace(sourceText, dictionary[sourceText]);
            } else if (currentLang === 'ar') {
                node.nodeValue = node.nodeValue.replace(text, sourceText);
            }
        }
    } else if (node.nodeType === Node.ELEMENT_NODE) {
        // Skip script and style tags
        if (node.tagName === 'SCRIPT' || node.tagName === 'STYLE') return;

        // Translate placeholders
        if (node.hasAttribute('placeholder')) {
            let pText = node.getAttribute('placeholder').trim();
            if (!originalAttrs.has(node)) {
                originalAttrs.set(node, { placeholder: pText });
            }
            let srcP = originalAttrs.get(node).placeholder;
            if (currentLang === 'en' && dictionary[srcP]) {
                node.setAttribute('placeholder', dictionary[srcP]);
            } else if (currentLang === 'ar') {
                node.setAttribute('placeholder', srcP);
            }
        }
        
        for (let child of node.childNodes) {
            translateTextNodes(child);
        }
    }
}

function updateLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    
    // Set direction
    document.documentElement.dir = lang === 'en' ? 'ltr' : 'rtl';
    document.documentElement.lang = lang;
    
    // Adjust CSS for font differences
    if (lang === 'en') {
        document.body.style.fontFamily = "'Segoe UI', Roboto, 'Helvetica Neue', Arial, sans-serif";
    } else {
        document.body.style.fontFamily = "'Cairo', sans-serif";
    }

    // Traverse and translate
    translateTextNodes(document.body);
    
    // Update button text
    const langNameSpan = document.getElementById('current-lang-name');
    if (langNameSpan) {
        langNameSpan.textContent = lang === 'en' ? 'English' : 'العربية';
    }
}

function toggleDropdown(e) {
    if(e) e.preventDefault();
    document.getElementById('lang-menu').classList.toggle('show');
}

function switchLang(lang, e) {
    if(e) e.preventDefault();
    document.getElementById('lang-menu').classList.remove('show');
    if(lang !== currentLang) {
        updateLanguage(lang);
    }
}

// Close the dropdown if the user clicks outside of it
window.onclick = function(event) {
  if (!event.target.closest('.lang-dropdown')) {
    var dropdowns = document.getElementsByClassName("lang-menu");
    for (var i = 0; i < dropdowns.length; i++) {
        if (dropdowns[i].classList.contains('show')) {
            dropdowns[i].classList.remove('show');
        }
    }
  }
}

// Ensure DOM is fully loaded before first translation pass
document.addEventListener('DOMContentLoaded', () => {
    updateLanguage(currentLang);
});
